    private void txtJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyReleased
        // TODO add your handling code here:
        Integer stok,hrgjual,jmljual,jmlharga;
         stok=Integer.parseInt(txtstok.getText());
         hrgjual=Integer.parseInt(txtHarga.getText());
         jmljual=Integer.parseInt(txtJumlah.getText());
         
         if(stok < jmljual){
             JOptionPane.showMessageDialog(rootPane, "Stok roti kurang");
             txtJumlah.setText("");
             txtTotal.setText("");
         }else{
             jmlharga=(hrgjual*jmljual);
             txtTotal.setText(String.valueOf(jmlharga));
             
         }
    }//GEN-LAST:event_txtJumlahKeyReleased

