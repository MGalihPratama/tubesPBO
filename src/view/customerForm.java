                 ps.executeUpdate(sql);
                 JOptionPane.showMessageDialog(rootPane, "Data disimpan");
                 tampilkan_data_transaksi();
                 
                 Integer stok, jumlah, sisa;
                 stok=Integer.parseInt(txtstok.getText());
